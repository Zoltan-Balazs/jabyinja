package com.zoltanbalazs.Own;

public class Arithmetic {
    public static void main(String[] args) {
        Integer int_a = 5;
        Integer int_b = -3;

        System.out.println(int_a + int_b);
        System.out.println(int_a - int_b);
        System.out.println(int_a * int_b);
        System.out.println(int_a / int_b);
        System.out.println(int_a % int_b);
        System.out.println(-int_a);
        System.out.println(-int_b);

        Long long_a = 5123125123123L;
        Long long_b = -3123165123123L;

        System.out.println(long_a + long_b);
        System.out.println(long_a - long_b);
        System.out.println(long_a * long_b);
        System.out.println(long_a / long_b);
        System.out.println(long_a % long_b);
        System.out.println(-long_a);
        System.out.println(-long_b);

        Float float_a = 5.2f;
        Float float_b = -3.8f;

        System.out.println(float_a + float_b);
        System.out.println(float_a - float_b);
        System.out.println(float_a * float_b);
        System.out.println(float_a / float_b);
        System.out.println(float_a % float_b);
        System.out.println(-float_a);
        System.out.println(-float_b);

        Double double_a = 5.21241251223123d;
        Double double_b = -3.8123125123d;

        System.out.println(double_a + double_b);
        System.out.println(double_a - double_b);
        System.out.println(double_a * double_b);
        System.out.println(double_a / double_b);
        System.out.println(double_a % double_b);
        System.out.println(-double_a);
        System.out.println(-double_b);
    }
}
